                int[] missingNumber;

                for (j = 0; j < numbers.length; j++)
                {
                    missingNumber.Add(numbers[j])
     

               while (missingNumber.contains(num))
                    {
                        num++;
                    }

                }


                return num;

            }

            int[] numbers = { 1, 3, 4, 5, 6 };

            num(numbers);
